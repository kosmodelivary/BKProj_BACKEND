package com.academy.delivery.service;

public class MemberDto {
	
	private String 	no;
	private String 	email;
	private String 	pass;
	private String 	name;
	private String 	tel;
	private String 	agreesms;
	private String 	agreeemail;
	
	//---------------------------------------------------
	// getter / setter
	//---------------------------------------------------
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getAgreesms() {
		return agreesms;
	}
	public void setAgreesms(String agreesms) {
		this.agreesms = agreesms;
	}
	public String getAgreeemail() {
		return agreeemail;
	}
	public void setAgreeemail(String agreeemail) {
		this.agreeemail = agreeemail;
	}

	
	
}